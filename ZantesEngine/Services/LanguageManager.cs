        private static readonly Dictionary<string, string> WordsTrToEn = BuildReverseWords(WordsEnToTr);

        public static UiLanguage CurrentLanguage { get; private set; } = UiLanguage.English;

        public static event Action? LanguageChanged;

        public static void SetLanguage(UiLanguage language)
        {
            if (CurrentLanguage == language)
                return;

            CurrentLanguage = language;
            LanguageChanged?.Invoke();
        }

        public static string T(string key)
        {
            var dict = CurrentLanguage == UiLanguage.English ? En : Tr;
            if (dict.TryGetValue(key, out var value))
                return value;

            return key;
        }

        public static void LocalizeTree(DependencyObject root)
        {
            if (root == null)
                return;

            Traverse(root);
        }

        public static string LocalizeLiteral(string input)
        {
            if (string.IsNullOrWhiteSpace(input))
                return input;

            if (!Regex.IsMatch(input, "[A-Za-z]", RegexOptions.CultureInvariant))
                return input;

            return CurrentLanguage == UiLanguage.Turkish
                ? Translate(input, PhrasesEnToTr, WordsEnToTr)
                : Translate(input, PhrasesTrToEn, WordsTrToEn);
        }

        private static string Translate(string input, IReadOnlyDictionary<string, string> phraseMap, IReadOnlyDictionary<string, string> wordMap)
        {
            if (phraseMap.TryGetValue(input, out var direct))
                return direct;

            return Regex.Replace(input, "[A-Za-z]+", m => TranslateWord(m.Value, wordMap));
        }

        private static string TranslateWord(string source, IReadOnlyDictionary<string, string> wordMap)
        {
            if (!wordMap.TryGetValue(source, out var translated))
                return source;

            if (string.IsNullOrEmpty(translated))
                return string.Empty;

            if (source.ToUpperInvariant() == source)
                return translated.ToUpperInvariant();

            if (char.IsUpper(source[0]))
                return char.ToUpperInvariant(translated[0]) + translated.Substring(1);

            return translated;
        }

        private static void Traverse(DependencyObject node)
        {
            if (node is TextBlock tb)
                tb.Text = LocalizeLiteral(tb.Text);
            else if (node is Run run)
                run.Text = LocalizeLiteral(run.Text);
            else if (node is Button btn && btn.Content is string btnText)
                btn.Content = LocalizeLiteral(btnText);
            else if (node is CheckBox cb && cb.Content is string cbText)
                cb.Content = LocalizeLiteral(cbText);
            else if (node is ComboBoxItem item && item.Content is string itemText)
            {
                if (item.Tag is string tag && (tag == "en" || tag == "tr"))
                {
                    item.Content = tag == "en" ? "English" : "Turkish";
                }
                else
                {
                    item.Content = LocalizeLiteral(itemText);
                }
            }
            else if (node is Label label && label.Content is string labelText)
                label.Content = LocalizeLiteral(labelText);

            int count = VisualTreeHelper.GetChildrenCount(node);
            for (int i = 0; i < count; i++)
                Traverse(VisualTreeHelper.GetChild(node, i));
        }

        private static Dictionary<string, string> BuildReverse(IReadOnlyDictionary<string, string> source)
        {
            var reverse = new Dictionary<string, string>(StringComparer.Ordinal);
            foreach (var pair in source)
            {
                if (!reverse.ContainsKey(pair.Value))
                    reverse[pair.Value] = pair.Key;
            }

            return reverse;
        }

        private static Dictionary<string, string> BuildReverseWords(IReadOnlyDictionary<string, string> source)
        {
            var reverse = new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase);
            foreach (var pair in source)
            {
                if (!string.IsNullOrWhiteSpace(pair.Value) && !reverse.ContainsKey(pair.Value))
                    reverse[pair.Value] = pair.Key;
            }

            return reverse;
        }
